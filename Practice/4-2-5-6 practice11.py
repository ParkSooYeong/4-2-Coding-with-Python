score = int(input("점수 : "))

if 100 >= score >= 95:
    print("A+ 입니다.")

elif 94 >= score >= 90:
    print("A0 입니다.")

elif 89 >= score >= 85:
    print("B+ 입니다.")

elif 84 >= score >= 80:
    print("B0 입니다.")

elif 79 >= score >= 75:
    print("C+ 입니다.")

elif 74 >= score >= 70:
    print("C0 입니다.")

elif 69 >= score >= 65:
    print("D+ 입니다.")

elif 64 >= score >= 60:
    print("D0 입니다.")

else:
    print("F 입니다.")
